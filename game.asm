jump:
	lw   $t0, jumpcount       	# Load jumpcount
   	bge  $t0, 2, abort_move 	# If jumpcount >=2 then no more jumps are left
   	
   	lw $t1, playerstate		
   	beq $t1, 2, air_jump		# If the playerstate is falling then we can jump in air
   	
   	beqz  $t0, jump_from_ground  	# If jumpcount is 0, this is the ground jump, if else its a double jump
   	
   	jal check_collision_above 	# Check to see if character is touching the roof before trying to jump
    	lw $t0, collision        
    	bnez  $t0, abort_move
    	
    	la $t0, playerstate		# Update the player state to jumping
	li $t1, 1
	sw $t1, 0($t0)
    	
    	la   $t0, jumpframes		# Reset the amount of frames the jump lasts
    	li   $t1, 0
    	sw   $t1, 0($t0)
    	
    	j    double_jump

	
jump_from_ground:
	jal check_collision_below 	# Check to see if charachter is touching the ground before trying to jump
    	lw $t0, collision        
    	beqz  $t0, abort_move
    	
    	jal check_collision_above 	# Check to see if character is touching the roof before trying to jump
    	lw $t0, collision        
    	bnez  $t0, abort_move
    	
    	la $t0, playerstate		# Update the player state to jumping
	li $t1, 1
	sw $t1, 0($t0)
	
	la $t0, jumpframes		# Reset the jump frames of the character
    	li $t1, 0
	sw $t1, 0($t0)
	
	double_jump:
		la $t0, playerstate	# Update the player state to jumping
		li $t1, 1
		sw $t1, 0($t0)
	
		la   $t0, jumpcount	# Add one to the jump count
    		lw   $t1, 0($t0)
    		addi $t1, $t1, 1
    		sw   $t1, 0($t0)
    		
    		j update_player_state
    	air_jump:
    		la   $t0, jumpframes	# Reset the jump frames of the character
    		li   $t1, 0
    		sw   $t1, 0($t0)
    	
    		la $t0, playerstate	# Update the player state to jumping
		li $t1, 1
		sw $t1, 0($t0)
	
		la   $t0, jumpcount 	# Set the jump count to 2 since we dont want to jump twice if  I started falling off a platform
    		lw   $t1, 0($t0)
    		addi $t1, $t1, 2
    		sw   $t1, 0($t0)
    		
    		j update_player_state
	
	
		
jumping_state:
	jal check_collision_above 	# Check to see if charachter is touching the ground before trying to jump
    	lw $t0, collision        
    	bnez  $t0, change_fall_state
	
	lw $t1, jumpframes
	bleu $t1, 7, jumping		# Move up 7 pixels maximum for a jump
	beq $t1, 14, change_fall_state	# Dont change state untill another 7 frames so the character floats at the peak of jump for a bit
	
	la $t0, jumpframes		# Update the jump frames of the character
    	lw $t1, jumpframes
    	addi $t1, $t1, 1		# Increment jump frames
	sw $t1, 0($t0)
	
	j update_enemy_state

jumping:
	jal erase_player      		# Erase current sprite by re-drawing the level background
    	lw $t7, curplayerlocation
    	addi $t7, $t7, -OFFSET     	# Move up by one pixel as a jump
    	sw $t7, curplayerlocation
    	jal draw_player                 # Draw sprite in the new location
    	
    	la $t0, jumpframes		# Update the jump frames of the character
    	lw $t1, jumpframes
    	addi $t1, $t1, 1         	# Increment jump frames
	sw $t1, 0($t0)
    	
    	j update_enemy_state

update_player_state:
	lw $t0, playerstate

	beq $t0, 1, jumping_state 	# Check if in jumpstate
	beq $t0, 2, falling_state 	# Check if in fallstate
	
	jal check_collision_below	# Check if character is on a platform
	lw $t1, collision
	bnez $t1, update_enemy_state 		# Character is standing
	
	j change_fall_state		# If the character is not on a platform then change to falling state
	
change_fall_state:
		la $t0, playerstate	# Update the player state to falling
		li $t1, 2
		sw $t1, 0($t0)
		
		j update_enemy_state
		
