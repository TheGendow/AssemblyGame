.text
.global main

main:

	li $t0, BASE_ADDRESS 		# $t0 stores the base address for display 
	la $t1, level        		# $t1 = pointer to level background
    	li $t2, MAX_PIXELS           	# $t2 = total number of pixels (64x64)
	

## Update the screen to the start of the level
make_level:
	li $t0, BASE_ADDRESS
	la $t1, level
	li $t2, MAX_PIXELS
	
	draw_level:
		beqz $t2, drawn_level	# If all pixels are drawn, exit loop

    		lw $t3, 0($t1)		# Load pixel color from level1
    		sw $t3, 0($t0)		# Store pixel color to display

    		addi $t0, $t0, 4	# Move to next display pixel
    		addi $t1, $t1, 4	# Move to next image pixel
    		subi $t2, $t2, 1	# Decrement pixel counter

    		j draw_level
    		
    	drawn_level:
    		lw $t0, spawnpoint	# Load spawn point
    		sw $t0, curplayerlocation # Update current player location to spawnpoint
    		jal draw_player		# Draw the players character
    		j game_loop 		# Replace with game loop later
# GAME LOOP #
game_loop:
    	li $t9, 0xffff0000 
    	lw $t8, 0($t9) 
    	beq $t8, 1, keypress_happened
    	
    	
    	li $v0, 32 
    	li $a0, DEFAULT_FRAME_RATE
	syscall
	
	j update_player_state
	j update_enemy_state
	j update_bullet_state
	j update_collisions
    
    	j game_loop       		# If no valid key pressed, loop
    	
respawn:
	# Set bullet locations 
	jal reset_bullet1
	jal reset_bullet2
	# Decrement health
	j make_level
    	
update_enemy_state:
	jal draw_shooter1
	jal draw_shooter2
	
	j update_bullet_state

update_bullet_state:
	j bullet_left
	j update_collisions

draw_bullet1:
    lw   $t0, locbullet1  		# Bullet location
    lw   $t1, bulletcolour              # Bullet colour
    sw   $t1, 0($t0)                    # Draw left pixel
    addi $t0, $t0, 4                    
    sw   $t1, 0($t0)                    # Draw right pixel
    jr   $ra
   
erase_bullet1:
    lw $s0, locbullet1   		# Bullet location
    li $t1, BASE_ADDRESS           	
    la $t2, level                       
    sub $t3, $s0, $t1 			# Curent bullet location - BASE_ADDRESS to find how much they differ by
    add $t2, $t2, $t3			# Add this difference to the address of the array index for the level
	
    lw $t4, 0($t2)                      # Background colour at bullet left pixel
    sw $t4, 0($s0) 			# Store the pixel to display
    
    
    addi $s0, $s0, 4  			# Position of right pixel on display
    addi $t2, $t2, 4                    
    lw $t4, 0($t2)			# Background colour at bullet right pixel
    sw $t4, 0($s0)			# Store the next pixel to the right to the display
   
    jr $ra

draw_bullet2:
    lw   $t0, locbullet2  		# Bullet location
    lw   $t1, bulletcolour              # Bullet colour
    sw   $t1, 0($t0)                    # Draw left pixel
    addi $t0, $t0, 4                    
    sw   $t1, 0($t0)                    # Draw right pixel
    jr   $ra
   
erase_bullet2:
    lw $s0, locbullet2   		# Bullet location
    li $t1, BASE_ADDRESS           	
    la $t2, level                       
    sub $t3, $s0, $t1 			# Curent bullet location - BASE_ADDRESS to find how much they differ by
    add $t2, $t2, $t3			# Add this difference to the address of the array index for the level
	
    lw $t4, 0($t2)                      # Background colour at bullet left pixel
    sw $t4, 0($s0) 			# Store the pixel to display
    
    
    addi $s0, $s0, 4  			# Position of right pixel on display
    addi $t2, $t2, 4                    
    lw $t4, 0($t2)			# Background colour at bullet right pixel
    sw $t4, 0($s0)			# Store the next pixel to the right to the display
   
    jr $ra
    
bullet_left:
	jal erase_bullet1       	# Erase bullet from previous position
	jal bullet_collision1
    	lw   $t1, locbullet1   		# Load current bullet location
    	addi $t1, $t1, -4               # Move left by 1 pixel (4 bytes)
    	sw   $t1, locbullet1   		# Save updated bullet location
    	jal draw_bullet1
    	
    	jal erase_bullet2       	# Erase bullet from previous position
	jal bullet_collision2
    	lw   $t1, locbullet2   		# Load current bullet location
